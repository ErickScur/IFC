    private void BtnCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarUsuarioActionPerformed
        // TODO add your handling code here:
        String nome = TextFieldNomeUsuario.getText();
        String id = TextFieldIdUsuario.getText();
        String email = TextFieldEmailUsuario.getText();
        String senha = FieldSenhaUsuario.getText();
        String confirmar = FieldConfirmarSenha.getText();
        int analista = ComboBoxAnalista.getSelectedIndex();    
        Boolean isAnalista;
        isAnalista = analista != 0;
        if(senha.equals(confirmar)){
            if(id.equals("Id")){
                usuarioDao.inserirUsuario(new Usuario(nome, email, senha, isAnalista));
                TextFieldIdUsuario.setText("Id");
                TextFieldNomeUsuario.setText("Nome do Usuario");
                TextFieldEmailUsuario.setText("Email do Usuario");
            }else{
                usuarioDao.atualizarUsuario(new Usuario(Integer.parseInt(id),nome, email, senha, isAnalista));
                TextFieldIdUsuario.setText("Id");
                TextFieldNomeUsuario.setText("Nome do Usuario");
                TextFieldEmailUsuario.setText("Email do Usuario");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "As senhas diferem!");
        }
        
        carregarTabelaUsuarios();
    }//GEN-LAST:event_BtnCadastrarUsuarioActionPerformed

