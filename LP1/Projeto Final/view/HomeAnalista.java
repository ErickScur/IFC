    public void carregarTabela(){
        String[] campos = {"id","Titulo","Urgencia","Categoria", "Cliente", "Data Criacao", "Status", "Responsavel"};
        DefaultTableModel dtm = new DefaultTableModel(campos, 0);
        this.tblChamados.setModel(dtm);
        if(RadioMostrarSomenteMeusChamados.isSelected())
            this.filtro.setAnalista(this.usuario.getId());
        List<Chamado> chamados = chamadoDao.consultarChamados(this.filtro);
        
        for(Chamado c: chamados){
            Categoria cat = categoriaDao.consultarCategoria(c.getIdCategoria());
            Usuario cli = usuarioDao.consultarUsuario(c.getIdUsuario());
            Usuario analista = usuarioDao.consultarUsuario(c.getIdAnalista());
            
            String nomeAnalista = "";
            if(analista != null)
                nomeAnalista = analista.getNome();
            
            String dataCriacao = chamadoDao.timeStamptoString(c.getDataCriacao());
            String status = chamadoDao.statusToString(c.getStatus());
            String id = String.valueOf(c.getId());
            String[] valores = {id, c.getTitulo(), c.getUrgencia(),cat.getNome(), cli.getNome(), dataCriacao, status, nomeAnalista};
            dtm.addRow(valores);
        }
    }
    private void carregarTabelaCategorias(){
        String[] campos = {"id", "Nome"};
        DefaultTableModel dtm = new DefaultTableModel(campos, 0);
        this.tblCategorias.setModel(dtm);
        List<Categoria> categorias = categoriaDao.consultarCategorias();
        for(Categoria c: categorias){
            String id = String.valueOf(c.getId());
            String[] valores = {id, c.getNome()};
            dtm.addRow(valores);
        }
    }
    private void carregarTabelaUsuarios(){
        String[] campos = {"id", "Nome", "Email","Tipo"};
        DefaultTableModel dtm = new DefaultTableModel(campos, 0);
        this.tblUsuarios.setModel(dtm);
        List<Usuario> usuarios = usuarioDao.consultarUsuarios();
        for(Usuario u: usuarios){
            String id = String.valueOf(u.getId());
            String tipo;
            if(u.isAnalista())
                tipo = "Analista";
            else
                tipo = "Cliente";
            String[] valores = {id, u.getNome(), u.getEmail(), tipo};
            dtm.addRow(valores);
        }
    }
