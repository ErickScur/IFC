    public void atualizaChamado(){
        LabelTitulo.setText(this.chamado.getTitulo());
        LabelDescricao.setText("<html><body>"+this.chamado.getCorpo()+"</body></html>");
        LabelCategoria.setText(categoriaDao.consultarCategoria(this.chamado.getIdCategoria()).getNome());
        LabelCliente.setText(usuarioDao.consultarUsuario(this.chamado.getIdUsuario()).getNome());
        LabelUrgencia.setText(this.chamado.getUrgencia());
        LabelDataCriacao.setText(chamadoDao.timeStamptoString(this.chamado.getDataCriacao()));
        TextAreaResposta.setText(this.chamado.getResposta());
        if(this.usuario.isAnalista()){
            BtnFinalizarAtendimento.setVisible(true);
            BtnIniciarAtendimento.setVisible(true);
            TextAreaResposta.setEnabled(true);
            LabelResposta.setText("Cadastrar Resposta");
        }else{
            BtnFinalizarAtendimento.setVisible(false);
            BtnIniciarAtendimento.setVisible(false);
            TextAreaResposta.setEnabled(false);
            LabelResposta.setText("Resposta Cadastrada:");
        }
        if(this.chamado.getIdAnalista() != 0)
            LabelAnalista.setText(usuarioDao.consultarUsuario(this.chamado.getIdAnalista()).getNome());
        switch(this.chamado.getStatus()){
            case 0:
                LabelDataFinalizacao.setText("Atendimento não iniciado!");
                BtnIniciarAtendimento.setEnabled(true);
                TextAreaResposta.setEnabled(false);
                BtnFinalizarAtendimento.setEnabled(false);
                break;
            case 1:
                LabelDataFinalizacao.setText("Em atendimento!");
                BtnIniciarAtendimento.setEnabled(false);
                TextAreaResposta.setEnabled(true);
                BtnFinalizarAtendimento.setEnabled(true);
                break;
            case 2:
                TextAreaResposta.setEnabled(false);
                BtnFinalizarAtendimento.setEnabled(false);
                BtnIniciarAtendimento.setEnabled(false);
                TextAreaResposta.setText(this.chamado.getResposta());
                LabelDataFinalizacao.setText(chamadoDao.timeStamptoString(this.chamado.getDataFinalizacao()));
                LabelResposta.setText("Resposta Cadastrada:");
                break;
        }
    }
    public void inicializaTela(Usuario usuario, Chamado chamado, HomeAnalista homeAnalista){
        this.usuario = usuario;
        this.chamado = chamado;
        this.homeAnalista = homeAnalista;
        atualizaChamado();
        pack();
        setLocationRelativeTo(null);
        setVisible(true);
    }
