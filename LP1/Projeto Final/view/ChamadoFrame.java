    private void BtnIniciarAtendimentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIniciarAtendimentoActionPerformed
        // TODO add your handling code here:
        this.chamado.setStatus(1);
        this.chamado.setIdAnalista(this.usuario.getId());
        chamadoDao.iniciarAtendimento(this.chamado.getId(), this.usuario.getId());
        atualizaChamado();
        this.homeAnalista.carregarTabela();
    }//GEN-LAST:event_BtnIniciarAtendimentoActionPerformed

    private void BtnFinalizarAtendimentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFinalizarAtendimentoActionPerformed
        // TODO add your handling code here:
        String resposta = TextAreaResposta.getText();
        this.chamado.setStatus(2);
        chamadoDao.finalizarAtendimento(this.chamado.getId(), resposta);
        this.chamado.setDataFinalizacao(new Timestamp(new java.util.Date().getTime()));
        atualizaChamado();
        this.homeAnalista.carregarTabela();
    }//GEN-LAST:event_BtnFinalizarAtendimentoActionPerformed

